console.clear();

// ------------------------ continue--------------------------

let i=0;
while(i<10){
    i++
    if(i==5){
        continue;
    }
    console.log(i);
}

// --------------------- for loop---------------------

for(let j = 0 ;j<=10;j++){
    if(j==5){
        continue;
    }
    console.log(j);
}